 * All numbers in [KNOWN_VALID_IMEIS] are 15-digit strings that pass the Luhn checksum.
 * No real device IMEI numbers are used — all constructed from arbitrary 14-digit prefixes.
 *
 * **Cross-validation process (Karan, 2026-05-02):**
