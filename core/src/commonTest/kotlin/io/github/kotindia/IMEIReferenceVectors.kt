 * 6. ALL 110 IMEIs passed npm luhn.validate() — ZERO divergences
 * 7. These npm luhn.validate()-verified IMEIs are the ground-truth in KNOWN_VALID_IMEIS
 * 8. 12 invalid vectors generated by +1 mod 10 on last digit; ALL failed npm luhn.validate()
